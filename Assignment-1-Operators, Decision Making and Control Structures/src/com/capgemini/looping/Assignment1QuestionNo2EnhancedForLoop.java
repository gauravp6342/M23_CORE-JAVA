package com.capgemini.looping;

public class Assignment1QuestionNo2EnhancedForLoop {

	public static void main(String[] args) 
	{
		String array[]= {"Your","First","Name"};
		
			for (String t:array)
			{
				System.out.println(t);
			}
		
		char c[]={'y','z'};
			for(int var:c)
			{
				System.out.println(var+" ");
			}
		
		int i[]={103,116,97};
			for(int var:i)
			{
				System.out.println((char)var+" ");
			}
			
	}

}
